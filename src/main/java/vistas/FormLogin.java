    private void Ingresar_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Ingresar_btActionPerformed
       String clave = new String(Clave_tb.getPassword()).trim();
        String usuario = Usuario_tb.getText().trim();

        conexionGlobal = new Conexion();
        conexionGlobal.ObtenerUsuario(usuario, clave);
        conexionGlobal.IniciarConexion();

        if (conexionGlobal.ValidarConexion()) {
            
            FormMenu ObjFormMenu = new FormMenu();
            ObjFormMenu.setFormLogin(this);
            ObjFormMenu.setVisible(true);
            this.setVisible(false);
            intentosFallidos = 0; 
        } else {
            intentosFallidos++;
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Usuario o contraseña incorrectos.\nIntento " + intentosFallidos + " de 3.", 
                "Error de inicio de sesión", 
                javax.swing.JOptionPane.ERROR_MESSAGE);

            // Si se superan los 3 intentos, se cierra el sistema
            if (intentosFallidos >= 3) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Has superado el número máximo de intentos.\nEl sistema se cerrará.", 
                    "Acceso bloqueado", 
                    javax.swing.JOptionPane.WARNING_MESSAGE);
                System.exit(0);
            }
        }
    }//GEN-LAST:event_Ingresar_btActionPerformed

