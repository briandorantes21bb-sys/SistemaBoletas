    // Validar campos vac√≠os
    JTextComponent[] campos = {
        CURP_tb, Nombre_tb, Apellido_tb, 
        NombreP_tb, ApellidoP_tb, CorreoP_tb, TelefonoP_tb, 
    };

    for (JTextComponent campo : campos) {
        if (campo.getText().trim().isEmpty()) {
            campo.setBackground(new java.awt.Color(255, 102, 102)); // rojo claro
            valido = false;
        } else {
            campo.setBackground(java.awt.Color.WHITE);
        }
    }

    if (Genero_tb.getSelectedIndex() == 0) {
        Genero_tb.setBackground(new java.awt.Color(255, 102, 102));
        valido = false;
    } else {
        Genero_tb.setBackground(java.awt.Color.WHITE);
    }

    if (!valido) {
        JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos obligatorios.", "Campos vac√≠os", JOptionPane.ERROR_MESSAGE);
        return;
    }

